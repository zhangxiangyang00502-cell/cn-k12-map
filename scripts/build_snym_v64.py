def main():
    # --- quiz.json ---
    qp = os.path.join(D, "quiz.json")
    qz = json.load(open(qp))
    before = len(qz["questions"])
    qz["questions"] = [q for q in qz["questions"] if not q.get("v64")]
    qz["questions"].extend(PLAIN_ADD)
    qz["questions"].extend(COMIC_QS)
    json.dump(qz, open(qp, "w"), ensure_ascii=False, indent=1)
    print(f"quiz.json: {before} -> {len(qz['questions'])}（+{len(PLAIN_ADD)} 普通，+{len(COMIC_QS)} 漫画题）")

    # --- packs_df.json ---
    pp = os.path.join(D, "packs_df.json")
    pk = json.load(open(pp))
    for tid in UNIT:
        pk["packs"][tid] = PACKS[tid]
    json.dump(pk, open(pp, "w"), ensure_ascii=False, indent=1)
    print(f"packs_df.json: 共 {len(pk['packs'])} 个补给包（少年有梦 7 节点已覆盖）")

    # --- comics_df.json ---
    cp = os.path.join(D, "comics_df.json")
    json.dump(COMICS, open(cp, "w"), ensure_ascii=False, indent=1)
    print(f"comics_df.json: {len(COMICS['comics'])} 知识漫画 + {len(COMICS['materials'])} 材料漫画")

    # --- 校验：图片引用齐全 ---
    missing = []
    for grp in ("comics", "materials"):
        for k, v in COMICS[grp].items():
            if not os.path.exists(os.path.join(ROOT, v["image"])):
                missing.append(v["image"])
    print("待生成图片：" + (", ".join(missing) if missing else "无"))


if __name__ == "__main__":
    main()
